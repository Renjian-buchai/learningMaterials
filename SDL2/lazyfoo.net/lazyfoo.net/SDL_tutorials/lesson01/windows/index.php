<div class="tutPreface"><h1 class="tutHead">Setting up SDL in Windows</h1>
<h6>Last Updated 2/18/12</h6>
<br>
An important note for <b>Visual Studio</b> users:<br>
<br>
The latest version of SDL for visual studio comes with two sets of library and binary files: x86 for 32bit, x64 for 64bit.<br>
<br>
If you're on a 64bit operating system, Visual Studio still compiles in 32bit by default. When you set the library directory, it should point to the x86 folder inside of the
lib folder.<br/>
<br/>

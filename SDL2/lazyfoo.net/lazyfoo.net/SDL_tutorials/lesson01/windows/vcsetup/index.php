<div class="tutPreface"><h1 class="tutHead">Setting up Visual Studio.NET</h1>
<h6>Last Updated 7/27/08</h6>
</div>

<div class="tutText">
<a class="tutLink" name="1" href="index.php#1">1)</a>First, Visual Studio.NET 2005 express users need to make sure you have
the latest build of Visual C++ 2005 express. It's available on <a class="tutLink" href="http://msdn.microsoft.com/vstudio/express/downloads/default.aspx">this page</a>.
Scroll down and download the installer and then run it:
<div class="tutImg"><img src="visual.jpg"></div>
If you have an older version of Visual C++ 2005 express, SDL will not work.<br>
<br>
<a class="tutLink" name="2" href="index.php#2">2)</a>Everybody needs to install the service pack 1 update.<br>
<br>
For Visual Studio.NET 2005 express users, it's available on the same page as Visual C++ express. It's towards the bottom of the page:
<div class="tutImg"><img src="servicepack05e.jpg"></div>
<br>
For Visual Studio.NET 2005 users, it's available on <a class="tutLink" href="http://msdn2.microsoft.com/en-us/vstudio/bb265237.aspx">this page</a>.
The download links are towards the bottom of the page:
<div class="tutImg"><img src="servicepack05.jpg"></div>
<br>
For Visual Studio.NET 2003 users, it's available on <a class="tutLink" href="http://www.microsoft.com/downloads/details.aspx?familyid=69d2219f-ce82-46a5-8aec-072bd4bb955e&displaylang=en">this page</a>.<br>
<br>
People tend to skip this because they have XP service pack 2, they think they don't need it. XP service packs and Visual C++ service packs are not the same thing so <b>make sure not to skip this step</b>.<br>
</div>


<div class="tutPreface"><h1 class="tutHead">Setting up SDL Extension Libraries</h1>
<div class="tutImg"><img src="preview.jpg"></div>
<h6>Last Updated 1/1/14</h6>

SDL only supports .bmp files natively, but using the SDL_image extension library, you'll be able to
load BMP, PNM, XPM, LBM, PCX, GIF, JPEG, TGA and PNG files.<br>
<br>
Extension libraries allow you to use features that basic SDL doesn't support natively. Setting up an SDL extension
library isn't hard at all, I'd even say it's easier to set up than basic SDL. This tutorial will teach you how to
use the SDL_image extension library.<br/>
<br/>

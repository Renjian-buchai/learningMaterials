    <h2>Where to Go From Here</h2>
If I did my job right
<ul>
 <li>OpenGL won't be as intimidating as an API to use.</li>
 <li>You know enough to make a 2D application with OpenGL.</li>
 <li>You should also be able to start learning OpenGL in 3D (hint: learn what the depth buffer does).</li>
 <li>You won't need to use the fixed function pipeline anymore.</li>
</ul>
<br/>
Now I send you on your way. Two sites that have helped me personally are
<a href="http://www.arcsynthesis.org/gltut/">Learning Modern 3D Graphics Programming</a> and
<a href="http://nopper.tv/opengl.html">OpenGL 3 and 4 with GLSL</a>. They use the modern programmable pipeline for their tutorials and they cover all sort of
subjects.<br/>
<br/>
If you haven't beef up your vector/matrix math, make sure to get on that. There are no shortcuts, you're going to make sure your mathmatics background is strong to be an effective
graphics programmer.<br/>
<br/>
OpenGL as an API has oceanic depth and a 36 tutorial set isn't enough cover what it can go in good enough detail. Hopefully now you managed to get off the ground which is the hardest
part when learning OpenGL.


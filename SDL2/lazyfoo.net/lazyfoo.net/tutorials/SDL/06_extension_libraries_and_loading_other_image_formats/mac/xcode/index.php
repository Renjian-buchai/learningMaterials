    <h1 class="text-center">Setting up SDL 2 on XCode 15.1</h1>
    
    <p class="text-center"><b>Last Updated: Jan 7th, 2024</b></p>
    
    
</div>
                    
                
                    
                        <div class="container border py-3 my-3">
    <a name="1" href="index.php#1">1)</a> First thing you need to do is download SDL_image framework. You will find them on the SDL_image website, specifically on
<a href="https://github.com/libsdl-org/SDL_image/releases">this page</a>.<br/>
<div class="text-center"><img class="img-fluid" alt="download page" src="../download.png"></div>
<br/>

<a name="2" href="index.php#2">2)</a> Open up the dmg and place the .framework folder alongside your xcodproj file.<br/>
<br/>

<a name="3" href="index.php#3">3)</a> Now go download the <a href="../../06_extension_libraries_and_loading_other_image_formats.zip">source for lesson 06</a>. Add the source files inside to your project.<br/>
<br/>
By default in the tutorials, the SDL headers are included like this:<br/>
<br/>
<code>#include &#060SDL/SDL_image.h&#062</code><br/>
<br/>

SDL on Mac OS X does things differently, so we have to include the SDL_image header like this:<br/>
<br/>
<code>#include &#060SDL2_image/SDL_image.h&#062</code><br/>
<br/>

For SDL_tff, we include the header like this:<br/>
<br/>
<code>#include &#060SDL2_ttf/SDL_ttf.h&#062</code><br/>
<br/>

For SDL_mixer, we include the header like this:<br/>
<br/>
<code>#include &#060SDL2_mixer/SDL_mixer.h&#062</code><br/>
<br/>

<a name="4" href="index.php#4">4)</a> Select your project and go to General -> Frameworks, Libraries, and Embedded Content -> click the plus sign to add items ->  Add Other -> Add Files -> select your SDL2_image.framework (or
SDL2_ttf.framework or SDL2_mixer.framework).<br/>
<br/>

<a name="5" href="index.php#5">5)</a> Don't forget to add the folder references for the image files for the tutorial. Now build. If there are any errors, make sure you didn't skip a step.<br/>


					<time class="container"><b><a name="2023-05-28-00" href="index.php#2023-05-28-00">May 28th, 2023</a></b></time><br/>
					If you can read this, it means the website migration is working. Yay =)<br/>
<br/>
What's changed? In terms of what you guys see, nothing drastic. The layout will no longer make your mobile device vomit and there's a nice dark/light mode toggle at the bottom of the page. Also added a search
bar to encourage Googling before emailing.<br/>
<br/>
What really took 6 months of what little free time I had was switching over to a data driven static site generator with a a proper deployment pipeline so I should no longer have to fix things on prod like I used to.
For example, the page <a href="tutorials/SDL/02_getting_an_image_on_the_screen/index.php">tutorials/SDL/02_getting_an_image_on_the_screen/index.php</a> php file looks like this:<br/>
<br/>
<code>---<br/>
     layout: tutorial<br/>
     tutorialSet: sdl2<br/>
     tutorial: 02_getting_an_image_on_the_screen<br/>
     ---<br/>
</code>
<br/>
The website is almost entirely data driven now. Learn this lesson I learned the hard way: you should throw out anything you made as an undergrad and be prepared to rebuild it from the ground up after you become a
professional. Any attempt to salvage it is just putting off the inevitable.<br/>
<br/>
While I was updating dozens of tutorials, I went an updated the logging article. It's not the most in depth article considering there are entire chapters in game engine architecture books that cover logs and entire
companies whose only product is something to manage logs, but it at least has some value in teaching logging as a debugging tool to students who are transitioning from purely text based application to graphical
ones.<br/>
<br/>
If you find any issues, please shoot me an email so I can fix it. My new email is on the FAQs page.

    <h2 class="accordion-header" id="heading4">
        <button type="button" class="accordion-button collapsed" data-bs-toggle="collapse" data-bs-target="#collapseoglAccordion4">Why are you using the fixed function pipeline? That makes you worse than Satan.</button>                     
    </h2>
    <div id="collapseoglAccordion4" class="accordion-collapse collapse" data-bs-parent="#oglAccordion">
        <div class="card-body">
            <br/>
            <p>Because I've tried to do an OpenGL tutorial set that uses programmable pipeline from the the start and the test audience just wasn't getting it. These tutorials aren't here to regurgitate theory to people who already
know graphics programming, they're here to help out beginners. Having concepts like GPU pipelines go over their heads isn't very helpful.<br/>
<br/>
So this tutorial set uses fixed function built in capabilites as a stepping stone to modern programmable pipelines. In the end, they're learning OpenGL 3.0+ code anyway. Besides, many of the code graphics algorithms 
(like Phong Shading), were developed in the 1970s. The algorithm hasn't changed, only the tools to implement it have.
</p>
        </div>

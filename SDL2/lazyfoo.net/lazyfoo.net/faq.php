        <button type="button" class="accordion-button collapsed" data-bs-toggle="collapse" data-bs-target="#collapsefrequentAccordion5">Can I use your code for an open source project?</button>                     
    </h2>
    <div id="collapsefrequentAccordion5" class="accordion-collapse collapse" data-bs-parent="#frequentAccordion">
        <div class="card-body">
            <br/>
            <p>I do allow my code to be used in certain cases (like a non-profit project for school if you ask for permission) and you can read more about it further along in the FAQs. I tend to be more lenient with
closed source projects.<br/>
<br/>
But I have a question for those of you that want to make your game open source: Why? And why do so many of you want to release it under GPL or LGPL or any other license that gives up a lot of your rights?<br/>
<br/>
I know there's a lot of cool projects that are open source, but what do you actually have to gain by releasing the source? Odds are your project won't be the Linux kernel of open source gaming and if it was super
popular, would you really like to give up your rights in case your game becomes the next Flappy Bird (which at one point was making $50,000 a day)?<br/>
<br/>
Some of you want to put up your personal projects on GitHub for a portfolio because you hope to get a job in the industry. There's usually three problems with using my source code:<br/>
<br/>
1) If an employer is looking at your GitHub, they want to see code you personally have made. Showing them projects that use a lot of another person's code is not a good look.<br/>
<br/>
2) A big mistake a lot of students make is that they put everything they ever made in their portfolio. Odds are if you're using my tutorials you've just barely finished your intro to C++ course. I've seen the application
process in different studios and they usually only spend a minute or two glancing over your work. You do not want them to see the gaming project you made after you just completed your intro to C++ course. Make
sure to only have your best work on there so you know they'll only see you at your best.<br/>
<br/>
3) The tutorials are designed to communicate key ideas of game programming the simplest way possible. The simplest code is not always the best structured code. For a real game project I would probably use an
Entity-Component-System Architecture (I recommend Googling that if you're interested in game engine architecture) but using an Entity-Component-System Architecture to teach basic texture rendering is just
going to bloat the code. However, in a real game project that you want to show off to a potential employer you're going to have the game engine structured like an operating system (because most game
engines are structured like operating systems) which means you want your code to be significantly different to the simplfied code in the tutorials.<br/>
<br/>
If you're still interested in using my source code for an open or closed source project <a href="faq.php#useSource" class="tutLink">you can find out more here</a>.

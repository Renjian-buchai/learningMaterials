</li>
<li>What are the costs of inheritance/polymorphism?</li>
<li>How are floating points represented in binary?</li>
<li>What is the deadly diamond?</li>
<li>If you C malloc memory then C++ delete it or C++ new memory and C free it, what happens?</li>
<li>What can happen if you do not bound your array copies? (Hint: it’s bad. Real bad)</li>
<li>How would you use a profiler to optimize code that is running slow?</li>
<li>Explain how a behavior tree works.</li>
<li>Explain how a blackboard works.</li>
<li>Explain how a nav mesh works.</li>
<li>Explain the fundamentals of the Tribes networking model.</li>
<li>Explain the fundamentals of rollback latency compensation.</li>
<li>Explain how TCP maintains in order communication of data.</li>
<li>What is a REST call?</li>
<li>Name two wire formats and the advantages/disadvantages between them.</li>
<li>What is vertical and horizontal scalability?</li>
<li>What is an Enterprise Service Bus? </li>
<li>What is a reverse proxy?</li>
<li>Explain the basics of public/private key cryptography</li>
<li>Name two different types of No SQL databases and what are the advantages/disadvantages between those and a SQL database.</li>
<li>What is virtualization, containerization, and orchestration and how would you use them in deploying a match making service?</li>
<li>You have a fresh AWS account (or Google Cloud Services/Azure), what infrastructure would you need to set up for an online pong game with around 50,000 daily active users?</li>
<li>What is a service level objective and a service level agreement?</li>
<li>How would you implement an anti cheat in a racing game?</li>
<li>How would you secure a match making API?</li>
</ul>

Keep in mind, these are not questions only for a lead or even software senior level games engineer. These are all questions I have seen for entry/mid level game engineering positions.<br/>

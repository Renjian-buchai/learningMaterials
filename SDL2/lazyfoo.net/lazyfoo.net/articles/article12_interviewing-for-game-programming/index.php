    <h1 class="text-center">Interviewing for Game Programming</h1>
    
    <p class="text-center"><b>Last Updated: Feb 14th, 2022</b></p>
    
        So you’ve managed to complete your computer science degree. You go to your school’s resume workshop to polish your resume and you start sending out applications. Then months later, you still get no callbacks. Why?<br/>
<br/>
Because you screwed up badly by starting your job applications process after you graduated.

    
    
</div>
                    
                
                    
                        <div class="container border py-3 my-3">
    Caution: Probable typos ahead.<br/>
<br/>
The strategy of blindly sending out resumes hoping one of them will bounce back (known as the spray and pray strategy) is the most common and one of the most ineffective ways to get a game programming job. To properly prepare for
applying for game industry job, it takes months if you’re really lucky but it will most likely take years. Waiting until senior year to start your game programming studies is way too late.<br/>
<br/>
In this article I will be covering what you need to know to get ready to apply for a game programming job. This is based off my personal and the experience of my immediate peers, so do take it with a grain of salt. If you want
to be a game artist, designer, producer, etc that will be a different path with different requirements so take this article with a massive mound of salt.<br/>
<br/>

<h2>Get Your Skills Up</h2>
As an undergrad, you should be spending at least (as in a minimum) 40 hours of week programming. Your classes count towards this time. This includes summer/winter/vacations. Summer doesn’t mean game backlog time, it’s personal
project time.<br/>
<br/>
For your freshman/sophomore year, you need to clear that first hurdle of getting something made with C++ and a low level API like SDL. You want to make sure to take a C++ course as early as you can in your academic career. I
recommend keeping the scope limited to something like Tetris. Don’t be afraid to do a couple of stepping stone projects like Tic Tac Toe or Blackjack before diving into Tetris. You may be tempted to make something like 16bit
era platformer for your first project, but everybody’s first few projects are giant heaps of spaghetti code that you will want to abandon. Your first projects should be about getting your hands dirty with the fundamentals of
game programming.<br/>
<br/>
In your sophomore/junior year, you should have completed your Tetris clone and be disgusted with how much of a crime against software engineering it is. Good job, many don’t get this far. Your next project will be a little
meatier. Here is where you may want to take on a 16bit era or 2.5D style platformer. Maybe you’ll even take a stab at making something 3D but simple like an early 3D era game like Quake. By now you should have completed linear
algebra and physics (also take these as soon as you can) and this would be a good time to apply those skills. This would also be a good time to get acquainted with an engine like Unity/Unreal so you can get used to their design
patterns. Either way, a good rule of thumb is to keep this project with a hard 1 year time limit. This project will also likely be a giant heap of spaghetti code and time spent trying to go back and fix your mistakes would be
more useful if you just started a new project having learned from your mistakes.<br/>
<br/> 
For your junior/senior year, there are two projects you want to get finished: your capstone game and your tech demo. Your capstone is a human ready game (meaning it should ready to present to people who aren’t programmers) that
you want to be the game your potential employers will see. You should have gotten your hands plenty dirty with your Tetris project and your 1 year time boxed project, so hopefully by now you know to keep scope limited so you
can make everything nice and polished. What you want this project to say is that you know what it takes to make a shippable game even if it’s small. If you can work with a team that also looks good, but make sure you work with
people who are decently experienced and also have worked on year long game projects.<br/>
<br/> 
The tech demo on the other hand is for programmers. As you were making your games, you got to touch on various things like graphics engines, physics engines, artificial intelligence, maybe even a little online multiplayer
programming. The tech demo doesn’t need to be pretty, but it should show that you have a deeper technical understanding of how a certain component of a game works. It could be something like a hand rolled graphics engine, a
hand rolled physics engine, a from scratch C++ game engine, a set of AI demos, remaking one of your old projects with a multiplayer online component and match making, etc. What you want this project to say is that you have deep
understanding of a specific domain of game programming.<br/>
<br/> 
If you’re not sure what to make (and even if you know what to make), I recommend following the conventional marketing wisdom of reasoning backwards and starting from what the market wants. Google game studios nearby
(“Los Angeles game studios”, “San Francisco Game Jobs”, etc) and see what they’re looking for. Get a good sized sample and gear it towards what they’re looking for. Try to balance it vs what you want to do. If some place is
looking for graphics programmers but you hate doing performance analysis, you’re better off doing something you’re more interested in.<br/>
<br/>

<h2>A Note About Unity</h2>
Before I continue, I want to explicitly state that I don’t hate Unity. If I were to open my own game studio, there’s a decent chance I would use it myself. People like to bash Unity, and I have been stuck in the studio past
midnight dealing with Unity issues, but every game engine has its issues. It’s a perfectly fine engine that has its ups and downs like any other engine.<br/>
<br/> 
However, a major problem with game engineers I have interviewed is that they are completely dependent on Unity. It is a red flag if I look at your portfolio and all I see is Unity projects. I do highly recommend having at least
one Unity project in your portfolio (along with an Unreal project) because there is a high demand for Unity skills, but I also need to see that you know how to operate with lower level C++. It is possible to be overly dependent
on Unreal Engine, but I have noticed engine dependency is more of an issue with Unity/C# game developers. If you end up working for a studio and they get handed a project that uses legacy code or some sort of hand rolled
engine, they need to know that you won’t be completely lost without Unity and C#.<br/>
<br/>

<h2>Prepare for Your Interview Questions</h2>

A very common interview question is “What have you been reading lately?”. I always carry a book on me because as a games engineer you’re always expected to stay current.<br/>
<br/> 
What books should you read? I generally refrain from giving book recommendations because 1) learning how to research is more important than me spoon feeding you’re a list of books to read and 2) I already get enough e-mails
from people wanting me to promote their book and I don’t want to encourage that.<br/>
<br/> 
But I will give you a list of subjects you should be familiar with:
<ul>
<li>The basics of graphics programming. Not everybody needs to be a Vulkan expert (that’s usually only for the graphics/engine specialists), but I would expect a game programmer to know the basics of vertex data, texturing,
lighting, shadows, shaders, and image post processing even if it just OpenGL 2.1.</li>
<li>Physics engine design. You should know how to at a conceptual level how a rigid body simulator is made. You probably don’t need to know things like fluid simulation unless you’re a specialist.</li>
<li>Game engine architecture. Game engines are like operating systems and it’s important to know why they are designed the way they are.</li>
<li>Advanced C++. I can nearly guarantee that your undergrad C++ course is not enough. Know what it takes to be effective with C++ and how to deal with C++ when you’re trying to squeeze every ounce of performance. Also learn
how C++’s unmanaged nature can lead to security issues.</li>
<li>Video game AI. It is different from the AI they typically teach in school. You probably won’t need to know machine learning (unless you’re a specialist), but you should know the basics of how to make bots for a player to
play against.</li>
<li>Multiplayer network programming. Almost every big title has an online component to it and you should be familiar with the fundamentals of latency compensation.</li>
<li>Software Engineering Fundamentals. You should know development processes even if they are not specific to game development. Know things like common searching/sorting algorithms, version control, design patterns, CI/CD,
agile, etc.</li>
<li>Backend development. With online games come things like match making. You don’t have to be an expert, but you should know at a conceptual level how to make a REST API.</li>
<li>Advanced databases. Again, your undergrad is probably not enough here. You should know about databases that aren’t just SQL databases and how having to deal with hundreds of thousands of concurrent users affects database
deployments.</li>
<li>Cloud infrastructure and Service Oriented Architecture. It’s OK if you aren’t an expert in AWS and Kubernetes, but you should know what they are and what they do.</li>
<li>Software security. With online game come hackers and you should know the basics of how to deal with them.</li>
<li>I am assuming you are going to an accredited computer science program that teaches the fundamentals of algorithm anaylsis, classical mechanics, linear algebra, computer architecture, databases, network programming and
operating system architecture. It is technically possible to get a game programming job without a degree, but you cannot afford to have any gaps in your knowledge.</li>
</ul>

How well should you know these things? Here are some real sample interview questions I have either received or given in interviews:
<ul>
<li>Explain how vertex data gets rasterized into pixels. For the sake of simplicity, let’s assume it’s a flat shaded mesh.</li>
<li>Explain how shadow mapping for a directional light works.</li>
<li>What’s the difference between forward and deferred rendering?</li>
<li>Explain how skeletal animation and skinning works. </li>
<li>What are the major differences between OpenGL/Direct3D11 and Vulkan/Direct3D12/Metal?</li>
<li>Explain how collision detection between two convex polygons works.</li>
<li>A gun fires a ray at a collision mesh, how would you tell where it hits a mesh? How would you optimize the collision detection?</li>
<li>Explain coarse/fine collision detection.</li>
<li>What is a contact? Explain a method of processing contacts.</li>
<li>Explain the differences between using Euler angles, rotation matrices, and quaternions for representing object orientation.</li>
<li>How can inline functions make code faster? How can it show it down?</li>
<li>Optimize this code without altering the contents of the functions:<br/>
<code><pre>void someFunctionA()
{
	while( somefunctionC() )
	{
		/*
		Pretend there's a whole bunch of code here
